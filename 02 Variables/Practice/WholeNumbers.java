public class WholeNumbers {
    public static void main (String[] args) {
        // 'long' data types need to be fixed with 'L' fo it to be rec ognised
        // Only use 'long' if data requires it
        long globalPopulation = 8_000_000_000L;
        System.out.println("The global population is " + globalPopulation);

        long dailyGoogleSearches = 85_000_000_000L;
        System.out.println("There are " + dailyGoogleSearches + " Google searches made daily!");
    }
}
