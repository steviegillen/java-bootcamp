public class Grades {
    public static void main(String[] args) {
        /*
        the 'char' data type uses 2 bytes of memory
        char variables must be surrounded with single quotes e.g. 'A'
        */
       char potions = 'A';
       char arithmancy = 'B';
       char charms = 'C';

       System.out.println("Potions: " + potions);
       System.out.println("Arithmancy: " + arithmancy);
       System.out.println("Charms: " + charms);
    }
}
