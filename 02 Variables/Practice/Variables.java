public class Variables {
    public static void main(String[] args){
        // updateing variables
        int passengers = 0;

        passengers += 9;
        System.out.printf("There are %d passengers on the bus\n", passengers);

        passengers -= 5;
        System.out.printf("There are %d passengers on the bus\n", passengers);

        passengers += 4;
        System.out.printf("There are %d passengers on the bus\n", passengers);

        passengers -= 8;
        System.out.printf("There are %d passengers on the bus\n", passengers);
    }
}
